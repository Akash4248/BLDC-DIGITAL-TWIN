float sin(float rad) {
  // Convert angle [0, 2PI) to index [0, 255]
  float wrapped = wrapAngle(rad);
  
  // 256 / (2*PI) = 40.743665f
  int index = (int)(wrapped * 40.743665f);
  
  // Boundary safety
  if (index < 0) index = 0;
  if (index > 255) index = 255;
  
  // Read from flash memory
  int8_t val = (int8_t)pgm_read_byte(&sineTable256[index]);
  
  // Convert back to float [-1.0, 1.0]
  // Note: 1/127 = 0.00787401f
  return (float)val * 0.00787401f;
}

